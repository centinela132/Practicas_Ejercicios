void main() {
  int cantidad = 90, precio = 600;
  var palabra1 = 'Informaticonfig', palabra2 = 'planeta';
  print(cantidad);

  // Operador de igualdad
  print(cantidad == precio);
  print('Palabra: ${palabra1 == palabra2}');

  // Operador de desigualdad
  print(cantidad != precio);

  // Operador mayor que
  print(cantidad > precio);

  // Operador menor que
  print(cantidad < precio);

  // Operador mayor o igual que
  print(cantidad >= precio);

  // Operador menor o igual que
  print(cantidad <= precio);
}
